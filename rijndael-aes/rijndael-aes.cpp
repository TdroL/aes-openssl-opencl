int main(int argc, char **argv)
{
	unsigned long rk[RKLENGTH(KEYBITS)];
	unsigned char key[KEYLENGTH(KEYBITS)];

	for (int i = 0; i < 6; i++)
	{
		memcpy(key, keys[i], KEYLENGTH(KEYBITS));
		memset(data, 0, DATA_LENGTH);

		int nrounds = rijndaelSetupEncrypt(rk, key, KEYBITS);

		printf("Key (%u bits):\n", KEYBITS);

		for (int k = 0; k < KEYLENGTH(KEYBITS); k++)
		{
			printf("0x%x, ", key[k]);
		}
		printf("\nCiphertext (%u bytes):\n", DATA_LENGTH/8);
		for (int j = 0; j < DATA_LENGTH; j += 16)
		{
			unsigned char plaintext[16];
			unsigned char ciphertext[16];

			memcpy(plaintext, &data[i][j], 16);

			rijndaelEncrypt(rk, nrounds, plaintext, ciphertext);

			for (int k = 0; k < 16; k++)
			{
				printf("0x%2x, ", ciphertext[k]);
			}
			printf("\n");
		}
		printf("\n");
	}

	return EXIT_SUCCESS;
}